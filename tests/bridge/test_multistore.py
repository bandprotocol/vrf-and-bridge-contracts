import pytest
from brownie import accounts, MockMultiStore


@pytest.fixture(scope="module")
def mockmultistore():
    return accounts[0].deploy(MockMultiStore)


def test_multistore_getapphash(mockmultistore):
    app_hash = mockmultistore.getAppHash(
        [
            "0x825DA85E632EEC90572718A2DDDC50C917E64226AD405218F3A419B01658A4E8",
            "0x6233CC684B6F6562575C7031E30E8763F54DFF765637FD2AA1FD3CBC33CB4636",
            "0x7A9DA2A861CA77BF28754896B07F92965D9937D949AC604B4B72B8EA818C56A3",
            "0xCD64197120739B45D87DF7371747924581AF4362046EAFBF461B677E24DD1926",
            "0xDF13F980CF14A32AF103438BF5A32535EE2D3B66B2DAD4852977D756F8233E1E",
            "0x0ABD414B7B9290D0075CD683B8DBED0C6B7E5D1162338FB8828B00E32E3C5F02",
        ]
    )
    assert app_hash == "0x04fd3b5c2824bccb50a6e2cc7714ea4f82e51ee83528abe831d5d8d679bd6a08"
